static int
first_hash_function(char first, char second, char third, char forth,
                    char fifth)
{
    return (first * 145 + second * 108 + third * 71 + forth * 42 +
            fifth * 142 - 44500) %
           6323;
}

static int
second_hash_function(char first, char second, char third, char forth,
                     char fifth)
{
    return (first * 138 + second * 65 + third * 114 + forth * 143 +
            fifth * 144 - 51911) %
           6941;
}

/* Returns 0 on fail, 1 on success */
static int
parse_color_from_colordict(PyObject *str_obj, Uint8 *r, Uint8 *g, Uint8 *b)
{
    Py_ssize_t name_len;
    const char *name = PyUnicode_AsUTF8AndSize(str_obj, &name_len);
    if (name_len < MIN_NAME_LENGTH || name_len > MAX_NAME_LENGTH) {
        return 0;
    }
    int h1 =
        first_hash_function(name[0], name[name_len - 1], name[name_len / 2],
                            name[name_len - 2], name[2]) %
        NUM_OF_COLORS;
    int index =
        (second_hash_function(name[0], name[name_len - 2], name[name_len / 2],
                              name[2], name[name_len - 1]) +
         displacements[h1]) %
        NUM_OF_COLORS;
    ColorStringStruct found = THECOLORS[index];
    if (!strcmp(name, found.name)) {
        *r = found.r;
        *g = found.g;
        *b = found.b;
        return 1;
    }
    return 0;
}
